package org.example.petstore.animalerie.enums;

/**
 * Enum representing the possible living environments for fish.
 * <p>
 * This enum defines two types of living environments that fish can inhabit:
 * <ul>
 *   <li><b>FRESH_WATER</b>: Represents fish that live in freshwater environments.</li>
 *   <li><b>SEA_WATER</b>: Represents fish that live in seawater environments.</li>
 * </ul>
 * </p>
 *
 * <p>Used for categorizing fish species based on their habitat type.</p>
 *
 * @author Your Name
 * @version 1.0
 */
public enum FishLivEnv {
    /**
     * Represents fish that live in freshwater environments, such as lakes and rivers.
     */
    FRESH_WATER,
    /**
     * Represents fish that live in seawater environments, such as oceans and seas.
     */
    SEA_WATER
}

